    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        // Lấy thông tin từ các trường nhập liệu
        String id = txtID.getText();// lấy id từ đâu đó, có thể là từ txtId hoặc một thành phần khác của giao diện
        // Tạo một đối tượng NhanVienQuanTri mới từ thông tin đã nhập
        NhanVienQuanTri qt = new NhanVienQuanTri();
        qt.setId(id);
        qt.setName(txtName.getText());
        qt.setEmail(txtEmail.getText());
        qt.setSdt(txtSdt.getText());

        // Gọi phương thức updateQT để cập nhật thông tin trong cơ sở dữ liệu
        int result = nvDAO.updateQT(qt);

        // Kiểm tra kết quả và thông báo cho người dùng
        if (result == 1) {
            DialogHelper.alert(this, "Cập nhật thành công");
        } else {
            DialogHelper.alert(this, "Cập nhật thất bại");
        }
        txtEmail.setEditable(false);
        txtName.setEditable(false);
        txtSdt.setEditable(false);
        btnSave.setVisible(false);
    }//GEN-LAST:event_btnSaveActionPerformed

