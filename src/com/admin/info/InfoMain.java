    /**
     * Thiết lập dữ liệu cho giao diện với thông tin của một Nhân viên Quản trị
     * dựa trên ID.
     *
     * @param id ID của Nhân viên Quản trị cần hiển thị thông tin.
     */
    public void setModel(String id) {
        // Lấy thông tin Nhân viên Quản trị từ cơ sở dữ liệu
        NhanVienQuanTri qt = nvDAO.selectQT(id);

        // Kiểm tra xem 'qt' có khác null không
        if (qt != null) {
            // Hiển thị thông tin của Nhân viên Quản trị trên giao diện
            txtID.setText(qt.getId());
            txtEmail.setText(qt.getEmail());
            txtName.setText(qt.getName());
            txtSdt.setText(qt.getSdt());

            // Kiểm tra xem 'qt.getHinh()' có khác null không trước khi tạo ImageIcon
            if (qt.getHinh() != null) {
                String hinh = qt.getHinh();
                // Đặt hình ảnh cho thành phần ImageIcon từ tệp hình ảnh
                imageAvatar1.setIcon(new ImageIcon(getClass().getResource("/mnm/images/" + hinh)));
            } else {
                // Đặt một hình ảnh mặc định hoặc thực hiện hành động thích hợp khi 'qt.getHinh()' là null
                imageAvatar1.setIcon(new ImageIcon(getClass().getResource("/mnm/images/logoproject2.png")));
            }
        } else {
            // Xử lý trường hợp 'qt' là null (ID không tồn tại)
            // Ví dụ: Hiển thị một thông báo hoặc thực hiện hành động thích hợp
            System.out.println("Không tìm thấy ID: " + id);
        }
    }

